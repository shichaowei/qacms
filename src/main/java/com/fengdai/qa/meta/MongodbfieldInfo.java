package com.fengdai.qa.meta;

public class MongodbfieldInfo {
	String field_id;
	String fieldname;
	String fieldvalue;
	String datasource;
	String fieldtype;
	String code;

	public String getField_id() {
		return field_id;
	}

	public MongodbfieldInfo setField_id(String field_id) {
		this.field_id = field_id;
		return this;
	}

	public String getFieldname() {
		return fieldname;
	}

	public MongodbfieldInfo setFieldname(String fieldname) {
		this.fieldname = fieldname;
		return this;
	}

	public String getFieldvalue() {
		return fieldvalue;
	}

	public MongodbfieldInfo setFieldvalue(String fieldvalue) {
		this.fieldvalue = fieldvalue;
		return this;
	}

	public String getDatasource() {
		return datasource;
	}

	public MongodbfieldInfo setDatasource(String datasource) {
		this.datasource = datasource;
		return this;
	}

	public String getFieldtype() {
		return fieldtype;
	}

	public MongodbfieldInfo setFieldtype(String fieldtype) {
		this.fieldtype = fieldtype;
		return this;
	}

	public String getCode() {
		return code;
	}

	public MongodbfieldInfo setCode(String code) {
		this.code = code;
		return this;
	}

}